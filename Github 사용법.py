## 맨 처음에 터미널을 먼저 열어야 함 ##

## 1. 초기화
# git init

## 2. 추가할 파일 더하기
# git add .

## 3. 히스토리 만들기(지금 버전에 대한 코멘트 남기기)
# git commit -m "코멘트"
# ex) git commit -m "텔레그램 채팅기능 추가"

## 4. Github Repository랑 연결
# git remote add origin https://github.com/MJeniuseful/Project-D.git

## 5. 잘 연결되었는지 확인
# git remote -v

## 6. 깃허브로 올리기
# git push origin master


